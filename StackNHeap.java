// Stack and Heap in Java
// every method call creates a new stack frame in the stack memory of jvm
// stack memory is used for method calls and local variables, while heap memory is used for objects and instance variables.
// stack memory is faster than heap memory, but it is limited in size, while heap memory is larger and can grow dynamically.
// stack memory is automatically managed by the jvm, while heap memory is managed by the garbage collector, which frees up memory that is no longer in use.
// stack memory is organized in a last-in-first-out (LIFO) manner, while heap memory is organized in a more complex manner, with objects being allocated and deallocated as needed. 
